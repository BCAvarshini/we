
public class Math {

    public static void main(String[] args) {
        int a = 15;
        int b = 5;
        int sum = a + b;
        int difference = a - b;
        int product = a * b;

        System.out.println("Addition: " + sum);
        System.out.println("Subtraction: " + difference);
        System.out.println("Multiplication: " + product);
    }

}
